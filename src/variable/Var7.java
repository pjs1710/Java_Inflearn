package variable;

public class Var7 {

    public static void main(String[] args) {

        // 직접 입력한 값들은 literal (리터럴)
        int a = 100;
        double b = 10.5;
        boolean c = true;
        char d = 'A';
        String e = "Hello Java";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
